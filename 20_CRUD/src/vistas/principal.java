    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        int fila = TablaDatos.getSelectedRow();

        if(fila == -1){
            JOptionPane.showMessageDialog(null, "NO SELECCIONADO");
        }else{
            id = Integer.parseInt((String)TablaDatos.getValueAt(fila, 0).toString());
            String nom = (String)TablaDatos.getValueAt(fila, 1);
            int tel = Integer.parseInt((String)TablaDatos.getValueAt(fila, 2).toString());

            txtCode.setText(""+id);
            txtName.setText(nom);
            txtCelular.setText(""+tel);
        }
    }//GEN-LAST:event_TablaDatosMouseClicked

