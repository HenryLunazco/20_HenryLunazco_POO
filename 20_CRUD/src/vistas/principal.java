    void listar(){
        String sql = "select * from persona";
        try
        {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            
            Object[]persona = new Object[6];
            modelo = (DefaultTableModel)TablaDatos.getModel();
            
            while(rs.next()){
                persona[0] = rs.getInt("id");
                persona[1] = rs.getString("nombre");
                persona[2] = rs.getInt("telefono");
                modelo.addRow(persona);
            }
            TablaDatos.setModel(modelo);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "ERROR LISTAR: "+e);
        }
        {
        }
    }
    
    void agregar(){
        String nombre = txtName.getText();
        String telefono = txtCelular.getText();
        String sql = "INSERT INTO persona (nombre, telefono) VALUES ('"+nombre+"', "+telefono+");";
        if(nombre.equals("")|| telefono.equals("")){
            JOptionPane.showMessageDialog(null, "CAJAS VACIAS");
        }else{
            try
            {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "USUARIO AGREGADO");
                limpiar();
            } catch (Exception e)
            {
            }
        }
    }
    
    void limpiar(){
        for(int i = 0; i <= TablaDatos.getRowCount(); i++){
            modelo.removeRow(i);
            i = i - 1;
        }
    }
    
    void modificar(){
        String nom = txtName.getText();
        String tel = txtCelular.getText();
        
        String sql = "UPDATE persona SET nombre = '"+nom+"', telefono = '"+tel+"'where id = "+id;
        
        if(nom.equals("")|| tel.equals("")){
            JOptionPane.showMessageDialog(null, "INGRESAR DATOS");
        }else{
            try
            {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "USUARIO ACTUALIZADO");
                limpiar();
            } catch (Exception e)
            {
            }
        }
    }
    
    void eliminar(){
        int filaSeleccionado = TablaDatos.getSelectedRow();
        
        if(filaSeleccionado == -1){
            JOptionPane.showMessageDialog(null, "DEBE SELECCIONAR FILA");
        }else{
            String sql = "DELETE FROM persona WHERE id = "+id;
            
            try
            {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "USUARIO ELIMINADO");
                limpiar();
            } catch (Exception e)
            {
            }
        }
    }
    
